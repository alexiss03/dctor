import {
  Count,
  CountSchema,
  Filter,
  FilterExcludingWhere,
  repository,
  Where,
} from '@loopback/repository';
import {
  post,
  param,
  get,
  getModelSchemaRef,
  patch,
  put,
  del,
  requestBody,
  response,
} from '@loopback/rest';
import {Event} from '../models';
import {EventRepository} from '../repositories';

export class AppointmentController {
  constructor(
    @repository(EventRepository)
    public calEventRepository : EventRepository,
  ) {}

  @post('/appointments')
  @response(200, {
    description: 'Event model instance',
    content: {'application/json': {schema: getModelSchemaRef(Event)}},
  })
  async create(
    @requestBody({
      content: {
        'application/json': {
          schema: getModelSchemaRef(Event, {
            title: 'NewEvent',
            exclude: ['id'],
          }),
        },
      },
    })
    calEvent: Omit<Event, 'id'>,
  ): Promise<Event> {
    return this.calEventRepository.create(calEvent);
  }

  @get('/appointments/count')
  @response(200, {
    description: 'Event model count',
    content: {'application/json': {schema: CountSchema}},
  })
  async count(
    @param.where(Event) where?: Where<Event>,
  ): Promise<Count> {
    return this.calEventRepository.count(where);
  }

  @get('/appointments')
  @response(200, {
    description: 'Array of Event model instances',
    content: {
      'application/json': {
        schema: {
          type: 'array',
          items: getModelSchemaRef(Event, {includeRelations: true}),
        },
      },
    },
  })
  async find(
    @param.filter(Event) filter?: Filter<Event>,
  ): Promise<Event[]> {
    return this.calEventRepository.find(filter);
  }

  @patch('/appointments')
  @response(200, {
    description: 'Event PATCH success count',
    content: {'application/json': {schema: CountSchema}},
  })
  async updateAll(
    @requestBody({
      content: {
        'application/json': {
          schema: getModelSchemaRef(Event, {partial: true}),
        },
      },
    })
    calEvent: Event,
    @param.where(Event) where?: Where<Event>,
  ): Promise<Count> {
    return this.calEventRepository.updateAll(calEvent, where);
  }

  @get('/appointments/{id}')
  @response(200, {
    description: 'Event model instance',
    content: {
      'application/json': {
        schema: getModelSchemaRef(Event, {includeRelations: true}),
      },
    },
  })
  async findById(
    @param.path.string('id') id: string,
    @param.filter(Event, {exclude: 'where'}) filter?: FilterExcludingWhere<Event>
  ): Promise<Event> {
    return this.calEventRepository.findById(id, filter);
  }

  @patch('/appointments/{id}')
  @response(204, {
    description: 'Event PATCH success',
  })
  async updateById(
    @param.path.string('id') id: string,
    @requestBody({
      content: {
        'application/json': {
          schema: getModelSchemaRef(Event, {partial: true}),
        },
      },
    })
    calEvent: Event,
  ): Promise<void> {
    await this.calEventRepository.updateById(id, calEvent);
  }

  @put('/appointments/{id}')
  @response(204, {
    description: 'Event PUT success',
  })
  async replaceById(
    @param.path.string('id') id: string,
    @requestBody() calEvent: Event,
  ): Promise<void> {
    await this.calEventRepository.replaceById(id, calEvent);
  }

  @del('/appointments/{id}')
  @response(204, {
    description: 'Event DELETE success',
  })
  async deleteById(@param.path.string('id') id: string): Promise<void> {
    await this.calEventRepository.deleteById(id);
  }
}
